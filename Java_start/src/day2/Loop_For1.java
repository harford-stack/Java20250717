package day2;

public class Loop_For1 {

	public static void main(String[] args) {
		
		// 반복문
		for(int i=1; i<10; i++) {
			System.out.println(i);
		}
System.out.println("---------------------------------");
		// 10부터 역순으로 1까지 출력
		for(int i=10; i>0; i--) {
			System.out.println(i);
		}
		
	}

}
